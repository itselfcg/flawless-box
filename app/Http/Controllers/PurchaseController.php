    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create($subcription, $address_id,$shippingDate, $monthNum)
    {

        $purchases = new PurchaseHistory();
        $purchases->account_id = $subcription->account_id;
        $purchases->subscription_id = $subcription->id;
        $purchases->address_id = $address_id;
        $purchases->status = BoxStatus::WAITING;
        $dateController = new DateController();
        $purchases->description = $dateController->getMonthName($monthNum);
        $purchases->shipping_date = $shippingDate;
        $purchases->save();

    }

